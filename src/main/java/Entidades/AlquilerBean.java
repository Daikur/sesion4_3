package Entidades;

public class AlquilerBean {

    private String nombrepelicula, extra, edad, numeroalquiler, formaPago;

    public String getNombrepelicula() {
        return nombrepelicula;
    }

    public void setNombrepelicula(String nombrepelicula) {
        this.nombrepelicula = nombrepelicula;
    }

    public String getExtra() {
        return extra;
    }

    public void setExtra(String extra) {
        this.extra = extra;
    }

    public String getEdad() {
        return edad;
    }

    public void setEdad(String edad) {
        this.edad = edad;
    }

    public String getNumeroalquiler() {
        return numeroalquiler;
    }

    public void setNumeroalquiler(String numeroalquiler) {
        this.numeroalquiler = numeroalquiler;
    }

    public String getFormaPago() {
        return formaPago;
    }

    public void setFormaPago(String formaPago) {
        this.formaPago = formaPago;
    }
}
